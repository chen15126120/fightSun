package ccn.medical.model;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

@Entity
@Table(name="doctor_info")
public class DoctorInfo {
	public int doctorId;
	public int usersId;
	public String doctorName;
	public int doctorSex;
	public Date doctorBirthday;
	public int departmentsId;
	public String positionalTitles;
	public String doctorIntroduction;
	public String doctorPhone;
	public String doctorEmail;
	public String doctorPassword;
	public String doctorAddress;
	public String doctorPicaure;
	public String doctorCertify;
	public String doctorAutograph;
	public int doctorStart = 2;
	public  String doctorQQ;
	
	
	
	private int hospitalId;
	private String hospitalName;
	private String departmentsName;
	
	
	@Column(name="hospital_id")
	public int getHospitalId() {
		return hospitalId;
	}
	public void setHospitalId(int hospitalId) {
		this.hospitalId = hospitalId;
	}
	@Column(name="hospital_name")
	public String getHospitalName() {
		return hospitalName;
	}
	public void setHospitalName(String hospitalName) {
		this.hospitalName = hospitalName;
	}
	@Column(name="departments_name")
	public String getDepartmentsName() {
		return departmentsName;
	}
	public void setDepartmentsName(String departmentsName) {
		this.departmentsName = departmentsName;
	}
	
	@Column(name="user_id")
	public int getUsersId() {
		return usersId;
	}
	public void setUsersId(int usersId) {
		this.usersId = usersId;
	}
	@Column(name="doctor_id")
	public int getDoctorId() {
		return doctorId;
	}

	public void setDoctorId(int doctorId) {
		this.doctorId = doctorId;
	}
	@Column(name="doctor_name")
	public String getDoctorName() {
		return doctorName;
	}
	public void setDoctorName(String doctorName) {
		this.doctorName = doctorName;
	}
	@Column(name="doctor_sex")
	public int getDoctorSex() {
		return doctorSex;
	}
	public void setDoctorSex(int doctorSex) {
		this.doctorSex = doctorSex;
	}
	@Column(name="doctor_birthday")
	public Date getDoctorBirthday() {
		return doctorBirthday;
	}
	public void setDoctorDirthday(Date doctorBirthday) {
		this.doctorBirthday = doctorBirthday;
	}
	@Column(name="departments_id")
	public int getDepartmentsId() {
		return departmentsId;
	}
	public void setDepartmentsId(int departmentsId) {
		this.departmentsId = departmentsId;
	}
	@Column(name="positional_titles")
	public String getPositionalTitles() {
		return positionalTitles;
	}
	public void setPositionalTitles(String positionalTitles) {
		this.positionalTitles = positionalTitles;
	}
	@Column(name="doctor_introduction")
	public String getDoctorIntroduction() {
		return doctorIntroduction;
	}
	public void setDoctorIntroduction(String doctorIntroduction) {
		this.doctorIntroduction = doctorIntroduction;
	}
	@Column(name="doctor_phone")
	public String getDoctorPhone() {
		return doctorPhone;
	}
	public void setDoctorPhone(String doctorPhone) {
		this.doctorPhone = doctorPhone;
	}
	@Column(name="doctor_email")
	public String getDoctorEmail() {
		return doctorEmail;
	}
	public void setDoctorEmail(String doctorEmail) {
		this.doctorEmail = doctorEmail;
	}
	@Column(name="doctor_password")
	public String getDoctorPassword() {
		return doctorPassword;
	}
	public void setDoctorPassword(String doctorPassword) {
		this.doctorPassword = doctorPassword;
	}
	@Column(name="doctor_address")
	public String getDoctorAddress() {
		return doctorAddress;
	}
	public void setDoctorAddress(String doctorAddress) {
		this.doctorAddress = doctorAddress;
	}
	@Column(name="doctor_picaure")
	public String getDoctorPicaure() {
		return doctorPicaure;
	}
	public void setDoctorPicaure(String doctorPicaure) {
		this.doctorPicaure = doctorPicaure;
	}
	@Column(name="doctor_certify")
	public String getDoctorCertify() {
		return doctorCertify;
	}
	public void setDoctorCertify(String doctorCertify) {
		this.doctorCertify = doctorCertify;
	}
	@Column(name="doctor_autograph")
	public String getDoctorAutograph() {
		return doctorAutograph;
	}
	public void setDoctorAutigraph(String doctorAutograph) {
		this.doctorAutograph = doctorAutograph;
	}
	@Column(name="doctor_start")
	public int getDoctorStart() {
		return doctorStart;
	}
	public void setDoctorStart(int doctorStart) {
		this.doctorStart = doctorStart;
	}
	@Column(name="doctor_qq")
	public String getDoctorQQ() {
		return doctorQQ;
	}
	public void setDoctorQQ(String doctorQQ) {
		this.doctorQQ = doctorQQ;
	}
	
}
